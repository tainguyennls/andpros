package com.example.nguyenhuutai.studentapp.model;

public class NewsModel {

    private String user;
    private String time;
    private String content;

    public NewsModel(){

    }

    public void setUser(String user) {
        this.user = user;
    }

    public String getUser() {
        return user;
    }

    public String getContent() {
        return content;
    }

    public String getTime() {
        return time;
    }


}
